package conditionalStatements;



public class SwitchCaseStatement {

	public static void main(String[] args) {
		
		
		
		int dayOfWeek =4  ;
		
		switch (dayOfWeek) {
		case 1 :
			System.out.println("Im on Leave");
			break;
		case 2 :
			System.out.println("Im on work");
			break;
		case 3 :
			System.out.println("Im playing cricket");
			break;
		case 4 :
			System.out.println("Im on Holiday");
			break;
			
			default :
				System.out.println("NILL");
		}

	}

}
